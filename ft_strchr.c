#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	a;

	a = (char)c;
	i = 0;
	while (s[i] || a == 0)
	{
		if (s[i] == a)
			return ((char *)s + i);
		i++;
	}
	return (NULL);
}
